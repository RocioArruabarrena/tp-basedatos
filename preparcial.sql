CREATE DATABASE INDUMENTARIA_CHIC;

USE INDUMENTARIA_CHIC;

--DDL
CREATE TABLE PRENDAS (
    PRE_ID INT NOT NULL,
    PRE_NOMBRES VARCHAR (50),
    PRE_TALLES INT,
    PRE_CATEGORIA VARCHAR (50),
    PRE_PRECIOS INT,
    PRE_COLORES VARCHAR (50),
    PRE_MATERIALES VARCHAR (50),
    PRE_SDC_ID INT
);

ALTER TABLE PRENDAS (
ADD CONSTRAINT (PK_PRE_ID) PRIMARY KEY (PRE_ID),
FOREIGN KEY PRE_SDC_ID REFERENCES SUCURSALES_DE_COMERCIOS
);

CREATE TABLE STOCKS (
    STO_ID INT NOT NULL,
    STO_PRE_ID INT,
    STO_CANT INT,
);

ALTER TABLE STOCKS (
    ADD CONSTRAINT (PK_STO_ID) PRIMARY KEY (STO_ID),
    FOREIGN KEY STO_PRE_ID REFERENCES PRENDAS 
);

CREATE TABLE SUCURSALES_DE_COMERCIOS(
    SDC_ID NUMBER NOT NULL,
    SDC_NOMBRE VARCHAR (60)
);

ALTER TABLE SUCURSALES_DE_COMERCIOS (
    ADD CONSTRAINT PK_SDC_ID PRIMARY KEY (SDC_ID)
);


